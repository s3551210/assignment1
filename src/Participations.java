
public class Participations {
	private String ID;
	private String name;
	private int age;
	private String state;
	
	public Participations (String ID, String name, int age, String state) {
		this.ID = ID;
		this.name = name;
		this.age = age;
		this.state = state;
	}

	public String getID() {
		return ID;
	}
}
